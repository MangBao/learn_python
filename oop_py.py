import this
from typing_extensions import Self
from unicodedata import name


class GiangVien:
    def __init__(self, name, address, sex):
        self.name = name
        self.address = address
        self.sex = sex
    
    def getName():
        return Self.name
    
    def getAddress():
        return Self.address
    
    def getSex():
        return Self.sex
    
    